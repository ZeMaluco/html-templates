			foreach ($stores as $store) {
				$store_data[] = array(
					'name' => $store['name'],
					'href' => $this->url->link('installer/installer/login', 'user_token=' . $this->session->data['user_token'] . '&installer_id=' . $result['installer_id'] . '&store_id=' . $result['store_id'], true)
				);
			}
			
			$data['installers'][] = array(
				'installer_id'    => $result['installer_id'],
				'name'           => $result['name'],
				'email'          => $result['email'],
				'installer_group' => $result['installer_group'],
				'status'         => ($result['status'] ? $this->language->get('text_enabled') : $this->language->get('text_disabled')),
				'ip'             => $result['ip'],
